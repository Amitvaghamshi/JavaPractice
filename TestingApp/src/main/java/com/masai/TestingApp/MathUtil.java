package com.masai.TestingApp;

public class MathUtil {
	
	/**
	 * 
	 * @param a
	 * @param b
	 * @return
	 */
	
	public int division(int a,int b){
		return a/b;
	}
	
	/**
	 * 
	 * @param a double
	 * @param b double type
	 * @return double 
	 */
	public double multiply(double a,double b) {
		return a*b;
	}
	
}
